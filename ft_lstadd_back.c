#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list new)
{
	t_list	*temp;

	if (!lst || !new)
		return ;
	if (*lst == NULL)
		*lst = new;
	else
	{
		temp = *lst;
		while (temp->next)
			temp = temp->new;
		temp->next = new;
	}
	new->next = NULL;
}
